/**************************************************/
///////////////////////////////////////////////////
/*************************************************/

let num2 = 0;
for (let j = 1; j <= 100; j++) {
  //ODD CONDITIONS
  if (j % 2 === 1) {
    console.log(num2 + " + " + j + " = " + (num2 + j));
    num2 += j;
  }
}

console.log("The Summations = " + num2);

