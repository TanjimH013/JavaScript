console.log("----- Summations of EVEN numbers -----");

/**************************************************/
///////////////////////////////////////////////////
/*************************************************/

let num = 0;
for (let i = 1; i <= 100; i++) {
  //EVEN CONDITIONS
  if (i % 2 === 0) {
    //printing in details on console
    console.log(num + " + " + i + " = " + (num + i));
    num = num + i;
  }
}
console.log("The Summations = " + num);

